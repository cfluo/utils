package com.yexuejc.util.ytest.hscf.api.model;

public class CarSensitive{
	/** 车架号 */
	private String vin;
	/** 车主手机号 */
	private String ownerMobile;
	/** 车主真实姓名 */
	private String ownerName;
	/** 车牌号 */
	private String lpn;
	private String lpnImg;
	private String vinImg;
	private String signImg;
	public String getVin() {
		return vin;
	}
	public void setVin(String vin) {
		this.vin = vin;
	}
	public String getOwnerMobile() {
		return ownerMobile;
	}
	public void setOwnerMobile(String ownerMobile) {
		this.ownerMobile = ownerMobile;
	}
	public String getOwnerName() {
		return ownerName;
	}
	public void setOwnerName(String ownerName) {
		this.ownerName = ownerName;
	}
	public String getLpn() {
		return lpn;
	}
	public void setLpn(String lpn) {
		this.lpn = lpn;
	}
	public String getLpnImg() {
		return lpnImg;
	}
	public void setLpnImg(String lpnImg) {
		this.lpnImg = lpnImg;
	}
	public String getVinImg() {
		return vinImg;
	}
	public void setVinImg(String vinImg) {
		this.vinImg = vinImg;
	}
	public String getSignImg() {
		return signImg;
	}
	public void setSignImg(String signImg) {
		this.signImg = signImg;
	}
	
}
